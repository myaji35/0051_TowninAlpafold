// ─────────────────────────────────────────────
// ICON BOOTSTRAP — [data-icon] 속성 자동 SVG 주입
// ─────────────────────────────────────────────
function bootstrapIcons() {
  document.querySelectorAll('[data-icon]').forEach(function(el) {
    if (el.querySelector('.feather-icon')) return; // 이미 주입됨
    var name = el.dataset.icon;
    var size = parseInt(el.dataset.iconSize || '18', 10);
    el.insertAdjacentHTML('afterbegin', window.getIcon(name, { size: size }));
  });
}

document.addEventListener('DOMContentLoaded', bootstrapIcons);

// 동적 렌더링 후 재호출 가능하도록 전역 노출
