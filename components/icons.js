/**
 * Feather line icon SVG 문자열 반환.
 * @param {string} name - icon key (위 FEATHER_PATHS 키)
 * @param {object} [opts] - { size=18, stroke=2, className='' }
 * @returns {string} SVG markup
 */
window.getIcon = function getIcon(name, opts) {
  const o = opts || {};
  const size = o.size || 18;
  const stroke = o.stroke || 2;
  const cls = o.className || 'feather-icon';
  const path = FEATHER_PATHS[name];
  if (!path) {
    console.warn('[icons] unknown icon:', name);
    return '<span class="feather-icon-missing" aria-hidden="true"></span>';
  }
  return `<svg class="${cls}" xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${stroke}" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${path}</svg>`;
};

